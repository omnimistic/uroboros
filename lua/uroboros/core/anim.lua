-- PRE-CALCULATION PHASE
-- This processes the positioning and color ONCE to save CPU cycles
local cached_frames = {}
local right_pad = string.rep(" ", 12) -- Horizontal offset
local down_pad = "\n\n"               -- Vertical offset

for i, frame in ipairs(frames) do
    local shifted = right_pad .. string.gsub(frame, "\n", "\n" .. right_pad)
    cached_frames[i] = clear_screen .. down_pad .. cosmic_cyan .. shifted .. reset_color
end

io.stdout:write(hide_cursor)

while true do
    for _, cached_data in ipairs(cached_frames) do

        io.stdout:write(cached_data)
        io.stdout:flush()
        
        -- adjust according to taste
        uv.sleep(200) 
    end
end